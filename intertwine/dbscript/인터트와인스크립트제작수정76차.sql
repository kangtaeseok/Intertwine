CREATE TABLE TB_POST (
  POST_ID NUMBER CONSTRAINT PK_POST_ID PRIMARY KEY,
  USER_ID VARCHAR2(50) CONSTRAINT NN_POST_USER_ID NOT NULL,
  POST_CONTENT VARCHAR2(600),
  POST_VISIBLE CHAR(1) CONSTRAINT CK_POST_VISIBLE CHECK (POST_VISIBLE IN ('0', '1', '2')),
  POST_TIME TIMESTAMP DEFAULT SYSDATE CONSTRAINT NN_POST_TIME NOT NULL,
  POST_VIEW NUMBER DEFAULT 0 CONSTRAINT NN_POST_VIEW NOT NULL,
  POST_PIN CHAR(1) DEFAULT '0' CONSTRAINT CK_POST_PIN CHECK (POST_PIN IN ('0', '1')),
  CONSTRAINT FK_POST_USER_ID FOREIGN KEY (USER_ID) REFERENCES TB_USERS(USER_ID) ON DELETE CASCADE
);
COMMENT ON COLUMN TB_POST.POST_ID IS '게시물ID';
COMMENT ON COLUMN TB_POST.USER_ID IS '사용자ID';
COMMENT ON COLUMN TB_POST.POST_CONTENT IS '게시물내용';
COMMENT ON COLUMN TB_POST.POST_VISIBLE IS '공개 여부 (0: 전체 공개, 1: 친구 공개, 2: 비공개)';
COMMENT ON COLUMN TB_POST.POST_TIME IS '게시시간';
COMMENT ON COLUMN TB_POST.POST_VIEW IS '조회수';
COMMENT ON COLUMN TB_POST.POST_PIN IS '핀여부 (0은 없음, 1은 있음)';

